public class GetterSetter {

		private String name;

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		@Override
		public String toString() {
			return "Test [name=" + name + "]";
		}
		public static void main(String[] args) {		
		}
	}

